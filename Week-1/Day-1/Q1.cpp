// Sum of 2 numbers

#include <iostream>
using namespace std;

int main()
{
    int a, b;
    cout << "enter a : ";
    cin >> a;

    cout << "enter b : ";
    cin >> b;

    int sum = a+b;
    cout << "sum = " << sum << endl;
    return 0;
}

